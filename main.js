const { app, BrowserWindow, dialog, ipcMain, shell, Menu, clipboard } = require('electron');
const path = require('path');
const fs = require('fs');

let win; // Variable globale pour la fen√™tre

function createWindow() {
  win = new BrowserWindow({
    width: 1700,
    height: 1080,
    minWidth: 900,
    minHeight: 600,
    icon: path.join(__dirname, 'icon.ico'),
    webPreferences: {
      preload: path.join(__dirname, 'preload.js'),
      contextIsolation: true,
      nodeIntegration: false,
      enableRemoteModule: false,
      webviewTag: true,
      sandbox: false
    }
  });

  win.loadFile('src/index.html');
  win.webContents.setWindowOpenHandler(({ url }) => {
    shell.openExternal(url);
    return { action: 'deny' };
  });

  // ‚Äî‚Äî‚Äî AUTO-DOWNLOAD & PROGRESSION + EXTRACTION AUTO + CORRECTION STRUCTURE ‚Äî‚Äî‚Äî
  win.webContents.session.on('will-download', async (event, item, webContents) => {
    let downloadsPath = await win.webContents.executeJavaScript('localStorage.getItem("downloads_path")');
    if (!downloadsPath) {
      downloadsPath = app.getPath('downloads');
    }
    const filename = item.getFilename();
    const fullPath = path.join(downloadsPath, filename);
    item.setSavePath(fullPath);

    item.on('updated', (evt, state) => {
      if (state === 'progressing') {
        const received = item.getReceivedBytes();
        const total = item.getTotalBytes();
        const percent = total ? Math.floor(received * 100 / total) : 0;
        win.webContents.send('nexus-download-progress', { percent, state: 'progressing' });
      }
    });

    item.once('done', async (evt, state) => {
      win.webContents.send('nexus-download-progress', { percent: 100, state: 'done', success: state === 'completed' });

      if (state === 'completed') {
        const ext = path.extname(fullPath).toLowerCase();
        if (['.zip', '.rar', '.7z'].includes(ext)) {
          let modsFolder = await win.webContents.executeJavaScript('localStorage.getItem("mods_path")');
          if (!modsFolder) modsFolder = downloadsPath;

          const modName = filename.replace(/\.(zip|rar|7z)$/i, '');
          const modTargetPath = path.join(modsFolder, modName);
          if (!fs.existsSync(modTargetPath)) fs.mkdirSync(modTargetPath);

          console.log('üöÄ D√©marrage extraction automatique pour:', modName);

          try {
            // üì¶ EXTRACTION avec correction automatique int√©gr√©e + d√©tection variantes
            const extractSuccess = await win.webContents.executeJavaScript(`
              (async () => {
                try {
                  console.log('üîß D√©but extraction pour: ${JSON.stringify(modName)}');
                  const result = await window.electronAPI.extractArchive(${JSON.stringify(fullPath)}, ${JSON.stringify(modTargetPath)});
                  console.log('üìä R√©sultat extraction:', result);
                  return result;
                } catch (error) {
                  console.error('‚ùå Erreur extraction:', error);
                  return false;
                }
              })()
            `);

            if (extractSuccess && extractSuccess.success) {
              if (extractSuccess.hasVariants) {
                console.log('üé≠ Variantes d√©tect√©es, attente s√©lection utilisateur...');
                // Le preload.js va envoyer l'√©v√©nement show-variant-selector
                // Le processus d'installation sera mis en pause
              } else {
                console.log('‚úÖ Extraction et correction termin√©es pour:', modName);
              }
            } else {
              console.error('‚ùå √âchec de l\'extraction pour:', modName);
            }

          } catch (error) {
            console.error('‚ùå Erreur lors du traitement automatique:', error);
          }

          // üóëÔ∏è SUPPRESSION DU FICHIER ARCHIVE (seulement si pas de variantes en attente)
          try {
            await win.webContents.executeJavaScript(`
              window.electronAPI.deleteFile(${JSON.stringify(fullPath)})
            `);
            console.log('üóëÔ∏è Archive supprim√©e:', fullPath);
          } catch (error) {
            console.error('‚ùå Erreur suppression archive:', error);
          }

          console.log('üèÅ Traitement automatique termin√© pour:', modName);
        }
      }
    });
  });

  // Directory selector
  ipcMain.handle('select-directory', async () => {
    const result = await dialog.showOpenDialog({ properties: ['openDirectory'] });
    if (result.canceled || !result.filePaths.length) return null;
    return result.filePaths[0];
  });

  // Image file selector
  ipcMain.handle('select-image-file', async () => {
    const result = await dialog.showOpenDialog({
      properties: ['openFile'],
      filters: [
        { name: 'Images', extensions: ['jpg', 'jpeg', 'png', 'webp'] }
      ]
    });
    if (result.canceled || !result.filePaths.length) return null;
    return result.filePaths[0];
  });

  // Handler pour r√©cup√©rer des √©l√©ments du localStorage
  ipcMain.handle('get-localStorage-item', async (event, key) => {
    try {
      const value = await win.webContents.executeJavaScript(`localStorage.getItem("${key}")`);
      return value;
    } catch (error) {
      console.error('Erreur r√©cup√©ration localStorage:', error);
      return null;
    }
  });

  // üÜï MENU CONTEXTUEL IMAGES - VERSION CORRIG√âE
  
  // Handler principal pour le menu contextuel d'images
  ipcMain.on('show-image-menu', (event, imageData) => {
    console.log('üñºÔ∏è Menu contextuel demand√© pour:', imageData.imageUrl);
    
    const template = [
      {
        label: 'üìã Copier l\'URL de l\'image',
        click: () => {
          clipboard.writeText(imageData.imageUrl);
          console.log('üìã URL copi√©e:', imageData.imageUrl);
          
          // Envoie une notification au renderer
          win.webContents.send('show-notification', {
            message: 'üìã URL copi√©e dans le presse-papier !',
            error: false
          });
        }
      },
      {
        label: '‚¨¨ T√©l√©charger pour un mod',
        click: () => {
          console.log('üîΩ Ouverture s√©lecteur mod pour:', imageData.imageUrl);
          
          // Envoie les donn√©es au renderer pour ouvrir le s√©lecteur
          win.webContents.send('open-mod-selector', {
            imageUrl: imageData.imageUrl,
            source: imageData.source || 'unknown'
          });
        }
      },
      { type: 'separator' },
      {
        label: 'üîó Ouvrir l\'image dans le navigateur',
        click: () => {
          shell.openExternal(imageData.imageUrl);
          console.log('üåê Image ouverte dans le navigateur:', imageData.imageUrl);
        }
      }
    ];

    const menu = Menu.buildFromTemplate(template);
    menu.popup({ window: win });
  });

  // üÜï HANDLER POUR S√âLECTEUR DE VARIANTES
  ipcMain.on('show-variant-selector', (event, data) => {
    console.log('üé≠ S√©lecteur de variantes demand√© pour:', data.modPath);
    console.log('üìã Variantes disponibles:', data.variants.length);
    
    // Envoie les donn√©es au renderer pour afficher le popup
    win.webContents.send('show-variant-selector', {
      modPath: data.modPath,
      variants: data.variants
    });
  });

  // Handler pour t√©l√©charger une image pour un mod
  ipcMain.handle('download-image-for-mod', async (event, imageUrl, modName) => {
    try {
      console.log('‚¨áÔ∏è T√©l√©chargement image pour mod:', modName, imageUrl);
      
      // R√©cup√®re les chemins des mods
      const modsPath = await win.webContents.executeJavaScript('localStorage.getItem("mods_path")') || '';
      const disabledModsPath = await win.webContents.executeJavaScript('localStorage.getItem("disabled_mods_path")') || '';
      
      // Cherche le mod dans les deux dossiers
      let targetPath = null;
      if (fs.existsSync(path.join(modsPath, modName))) {
        targetPath = path.join(modsPath, modName);
      } else if (fs.existsSync(path.join(disabledModsPath, modName))) {
        targetPath = path.join(disabledModsPath, modName);
      }
      
      if (!targetPath) {
        return { success: false, error: 'Mod non trouv√© dans les dossiers configur√©s' };
      }

      // T√©l√©charge l'image
      const https = require('https');
      const http = require('http');
      
      const success = await new Promise((resolve) => {
        const dest = path.join(targetPath, 'preview.jpg');
        const proto = imageUrl.startsWith('https') ? https : http;
        
        const req = proto.get(imageUrl, (res) => {
          if (res.statusCode !== 200) {
            console.error('‚ùå Erreur HTTP:', res.statusCode);
            return resolve(false);
          }
          
          const stream = fs.createWriteStream(dest);
          res.pipe(stream);
          
          stream.on('finish', () => {
            stream.close();
            console.log('‚úÖ Image t√©l√©charg√©e:', dest);
            resolve(true);
          });
          
          stream.on('error', (err) => {
            console.error('‚ùå Erreur √©criture:', err);
            fs.unlink(dest, () => {});
            resolve(false);
          });
        }).on('error', (err) => {
          console.error('‚ùå Erreur t√©l√©chargement:', err);
          resolve(false);
        });
      });

      return { success, modPath: targetPath };
      
    } catch (error) {
      console.error('‚ùå Erreur t√©l√©chargement image:', error);
      return { success: false, error: error.message };
    }
  });

  // Optionnel: Ouvre devtools si besoin
  // win.webContents.openDevTools();
  
  return win;
}

app.whenReady().then(createWindow);

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') app.quit();
});
